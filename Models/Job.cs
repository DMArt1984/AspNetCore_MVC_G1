namespace AspNetCore_MVC_Project.Models
{
    /// <summary>
    /// Модель данных для таблицы Job.
    /// Используется для хранения информации о рабочих заданиях или позициях.
    /// </summary>
    public class Job
    {
        /// <summary>
        /// Уникальный идентификатор записи в таблице Job.
        /// Автоматически генерируется базой данных.
        /// </summary>
        public int Id { get; set; }

        /// <summary>
        /// Название или описание работы.
        /// Например: "Инженер", "Менеджер", "Оператор станка".
        /// </summary>
        public string Title { get; set; }

        /// <summary>
        /// Аналоговое значение, связанное с работой.
        /// Может использоваться для хранения числовых данных (например, зарплата, коэффициент важности).
        /// </summary>
        public decimal AnalogValue { get; set; }
    }
}

