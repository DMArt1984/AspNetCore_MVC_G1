namespace AspNetCore_MVC_Project.Models.Factory
{
    /// <summary>
    /// Модель данных для таблицы Statistic.
    /// Используется для хранения статистических данных и коэффициентов.
    /// </summary>
    public class Statistic
    {
        /// <summary>
        /// Уникальный идентификатор записи в таблице Statistic.
        /// Автоматически генерируется базой данных.
        /// </summary>
        public int Id { get; set; }

        /// <summary>
        /// Название или описание статистического параметра.
        /// Например, "Производительность", "Эффективность", "Рентабельность".
        /// </summary>
        public string Title { get; set; }

        /// <summary>
        /// Первый коэффициент (Kf1), связанный с данным параметром.
        /// Может представлять собой процентное или числовое значение.
        /// </summary>
        public double Kf1 { get; set; }

        /// <summary>
        /// Второй коэффициент (Kf2), связанный с данным параметром.
        /// Используется для расчетов или сравнений.
        /// </summary>
        public double Kf2 { get; set; }

        /// <summary>
        /// Третий коэффициент (Kf3), связанный с данным параметром.
        /// Может использоваться для многомерного анализа данных.
        /// </summary>
        public double Kf3 { get; set; }
    }
}

